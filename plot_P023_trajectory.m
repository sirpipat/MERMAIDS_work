function plot_P023_trajectory
% NO INPUT
%
% plot P023 trajectory during 13 Sep 2018 to 15 Aug 2019
%
% Last edited by Sirawich Pipatprathanporn, 11/13/2019

    T = readtable('/home/sirawich/research/raw_data/metadata/P023_all.txt');
    T.Properties.VariableNames = {'Station', ...
                                  'Date', ...
                                  'Time', ...
                                  'stla', ...
                                  'stlo', ...
                                  'HDil', ...
                                  'VDil', ...
                                  'VBat', ...
                                  'Vmin', ...
                                  'PInt', ...
                                  'PExt', ...
                                  'PRange', ...
                                  'NumCommand', ...
                                  'NumQueued', ...
                                  'NumUploaded'};
    
    [C, ia] = unique(T.Date);
    TC = T(ia,:);

    c_str = string(C);

    plot(TC.stlo(4:49),TC.stla(4:49),'.-k');
    text(TC.stlo(4)-0.2,TC.stla(4)-0.03,c_str(4,:));
    text(TC.stlo(27)-0.2,TC.stla(27)-0.03,c_str(27,:));
    text(TC.stlo(49)+0.03,TC.stla(49),c_str(49,:));
    ylabel('latitude (degrees)');
    title('P023 Trajectory');
    xlabel('longitude (degrees)');
end